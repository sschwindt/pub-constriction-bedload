% Create xlabel
xlabel('Constriction ratio [-]','FontSize',fontS,'FontName','Arial');
% Create ylabel
ylabel('Backwater ratio h_* [-]',...
    'FontSize',fontS,...
    'FontName','Arial');
annotation(figure1,'textbox',...
        [xPos 0.815  0.4 0.1],... % [x_begin y_begin length height]
        'String',{'a)'},...
        'FontName','Arial','FontSize',fontS+2,...
        'FontWeight','bold',...
        'FitBoxToText','on',...
        'BackgroundColor',[1 1 1],...
        'LineStyle','none');

