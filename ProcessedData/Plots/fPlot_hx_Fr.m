    'Color',[0.,0.,0.],...
    'LineWidth',1,'LineStyle',lStyles{1,4},...
    'Marker',mStyles{1,4},'MarkerSize',MarkerS,...
    'DisplayName','Regression curve');

% lower limits -----
% create plot of  regression curve a*
plot2(10) = plot(xInterphx(:,3),lyInterpFr(:,3),...
    'Color',limColor,...
    'LineWidth',1,'LineStyle',lStyles{1,5},...
    'Marker',mStyles{1,4},'MarkerSize',MarkerS,...
    'DisplayName','Regression curve');
% create plot of regression curve b*
plot2(11) = plot(xInterphx(:,2),lyInterpFr(:,2),...
    'Color',limColor,...
    'LineWidth',1,'LineStyle',lStyles{1,5},...
    'Marker',mStyles{1,4},'MarkerSize',MarkerS,...
    'DisplayName','Regression curve');
% create plot of regression curve a*b*
plot2(12) = plot(xInterphx(:,1),lyInterpFr(:,1),...
    'Color',limColor,...
    'LineWidth',1,'LineStyle',lStyles{1,5},...
    'Marker',mStyles{1,4},'MarkerSize',MarkerS,...
    'DisplayName','Regression curve');

% upper limits -----
% create plot of  regression curve a*
plot2(13) = plot(xInterphx(:,3),uyInterpFr(:,3),...
    'Color',limColor,...
    'LineWidth',1,'LineStyle',lStyles{1,5},...
    'Marker',mStyles{1,4},'MarkerSize',MarkerS,...
    'DisplayName','Regression curve');
% create plot of regression curve b*
plot2(14) = plot(xInterphx(:,2),uyInterpFr(:,2),...
    'Color',limColor,...
