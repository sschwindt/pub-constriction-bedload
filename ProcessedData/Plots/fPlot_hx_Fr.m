fMakeYgrid([0,1],0.2:0.2:08,0.5);

% Create xlabel
xlabel('Constriction ratio [-]','FontSize',fontS,'FontName','Arial');
% Create ylabel
ylabel('Upstream Froude number Fr_0 [-]',...
    'FontSize',fontS,...
    'FontName','Arial');  
annotation(figure1,'textbox',...
        [xPos 0.34  0.4 0.1],... % [x_begin y_begin length height]
        'String',{'b)'},...
        'FontName','Arial','FontSize',fontS+2,...
        'FontWeight','bold',...
        'FitBoxToText','on',...
        'BackgroundColor',[1 1 1],...
        'LineStyle','none');

